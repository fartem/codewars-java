package com.smlnskgmail.jaman.codewarsjava.kyu8;

// https://www.codewars.com/kata/59342039eb450e39970000a6
public class CountOddNumbersBelowN {

    private final int input;

    public CountOddNumbersBelowN(int input) {
        this.input = input;
    }

    public int solution() {
        return input / 2;
    }

}
